function add(a, b){
  return a + b;
}
function subtract(a, b){
  return a - b;
}
function mulitply(a, b){
  var num = a * b;
  return num;
}
function divide(a, b){
  return a / b;
}
function increment(n){
  n++;
  return n;
}
function decrement(n){
  n--;
  return n;
}
function makeInt(string){
  var x = parseInt(string, 10);
  return x;
}
